package com.recruiter.bean;

public class Demand {
	private String username;
	private String demand_field;
	private String demand_job;
	private String demand_jobname;
	private String demand_salary_min;
	private String demand_salary_max;
	private String demand_experience;
	private String id_cloud;
	private String demand_detail;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getDemand_field() {
		return demand_field;
	}
	public void setDemand_field(String demand_field) {
		this.demand_field = demand_field;
	}
	public String getDemand_job() {
		return demand_job;
	}
	public void setDemand_job(String demand_job) {
		this.demand_job = demand_job;
	}
	public String getDemand_salary_min() {
		return demand_salary_min;
	}
	public void setDemand_salary_min(String demand_salary_min) {
		this.demand_salary_min = demand_salary_min;
	}
	public String getDemand_salary_max() {
		return demand_salary_max;
	}
	public void setDemand_salary_max(String demand_salary_max) {
		this.demand_salary_max = demand_salary_max;
	}
	public String getDemand_experience() {
		return demand_experience;
	}
	public void setDemand_experience(String demand_experience) {
		this.demand_experience = demand_experience;
	}
	public String getId_cloud() {
		return id_cloud;
	}
	public void setId_cloud(String id_cloud) {
		this.id_cloud = id_cloud;
	}
	public String getDemand_detail() {
		return demand_detail;
	}
	public void setDemand_detail(String demand_detail) {
		this.demand_detail = demand_detail;
	}
	public String getDemand_jobname() {
		return demand_jobname;
	}
	public void setDemand_jobname(String demand_jobname) {
		this.demand_jobname = demand_jobname;
	}
	
	
}
